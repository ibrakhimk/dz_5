public class Boss {
         private String defenceBoss;
         private int healthBoss;
         private int damageBoss;
        Boss(){
        }
    public String getDefenceBoss() {
        return this.defenceBoss;
    }
    public int getHealthBoss() {
        return this.healthBoss;
    }
    public int getDamageBoss(){
        return this.damageBoss;
    }
    public void setDefenceBoss(String defenceBoss) {
        this.defenceBoss = defenceBoss;
    }
    public void setHealthBoss(int healthBoss) {
        this.healthBoss = healthBoss;
    }
    public void setDamageBoss(int damageBoss) {this.damageBoss = damageBoss;}
}





